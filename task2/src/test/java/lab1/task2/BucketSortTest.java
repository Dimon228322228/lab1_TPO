    @Test
    void testLargeRandomArray() {
        int size = 100;
        double[] input = new double[size];
        Random rand = new Random(42); // фиксированный seed для воспроизводимости
        for (int i = 0; i < size; i++) {
            input[i] = rand.nextInt(1000) + 1; // натуральные от 1 до 1000
        }

        double[] result = sorter.sort(input);
        // Проверяем, что результат отсортирован
        for (int i = 0; i < result.length - 1; i++) {
            assertTrue(result[i] <= result[i + 1], "Массив не отсортирован");
        }

        // Проверяем структуру лога
        List<String> log = sorter.getLog();
        assertTrue(log.get(0).equals("A"), "Лог должен начинаться с A");
        assertTrue(log.get(log.size() - 1).equals("E"), "Лог должен заканчиваться на E");

        // Проверяем, что количество записей C равно размеру массива
        long countC = log.stream().filter(line -> line.startsWith("C:")).count();
        assertEquals(size, countC, "Количество записей C должно быть равно длине массива");

        // Проверяем, что после каждой непустой корзины есть запись D
        // Для этого можно убедиться, что количество записей D не превышает количество корзин
        long countD = log.stream().filter(line -> line.startsWith("D:")).count();
        assertTrue(countD <= size, "Записей D не может быть больше числа корзин");
        assertTrue(countD > 0, "Должна быть хотя бы одна запись D");
    }

